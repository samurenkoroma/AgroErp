import {Geometry, Metadata} from '@/entities/shared/types';
import {Dimensions} from "@/entities/spatial/production-unit/dto.ts";

export interface ProductionUnit {
    id: string;
    parentId?: string;
    area: number;
    status: 'growing' | 'preparation' | 'empty';
    geometry?: Geometry;
    type: ProductionUnitType;
    code: string;
    name: string;
    climateZoneId?: string;
    properties: Properties;
    children?: ProductionUnit[];
    schema: Element[];
}

export interface Element {
    status: 'growing' | 'preparation' | 'empty';
    id: string;
    type: string;
    x: number;
    y: number;
    area: number;
    width: number;
    length: number;
    name: string;
    code: string;
}


export interface Properties {
    schema: any;
    dimensions?: Dimensions
    capabilities?: Capability[];
    metadata?: Metadata;
    capacity: {
        areaM2: number,
        channelCount: number,
        plantCapacity: number,
        trayCount: number,
        waterVolumeLiters: number
    },

    position?: {
        X: number,
        Y: number,
        Z?: number
    }
}

export type Capability =
    | 'SOIL' | 'IRRIGATION' | 'FERTIGATION' | 'DRAINAGE'
    | 'HYDROPONIC' | 'AEROPONIC' | 'NUTRIENT_CONTROL'
    | 'LIGHTING' | 'CLIMATE_CONTROL'
    | 'SENSOR_SUPPORT' | 'AUTOMATION'
    | 'SLOT_BASED' | 'MOBILE';

/**
 * Производственная единица (Production Unit) - физический объект,
 * на котором осуществляется выращивание культур.
 *
 * Все типы организованы в иерархию: от крупных (поля) до мелких (слоты).
 */
export type  RootUnits = 'FIELD' | 'GREENHOUSE' | 'CONTAINER' | 'STORAGE' | 'PLOT'
export type ProductionUnitType =
// ========== ПОЛЕВЫЕ ТИПЫ (Open Field) ==========
/**
 * FIELD - Поле
 * Самая крупная единица в открытом грунте.
 * Имеет геометрию (полигон), площадь в гектарах.
 * Может содержать: BLOCK, BED, ROW
 * Пример: "Поле Северное", 50 га
 */
    'FIELD' |
    /**
     * PLOT - Участок
     * Небольшой участок земли (приусадебный, дачный, фермерский).
     * Гибридный тип: может использоваться как открытый грунт, так и для установки
     * гидропонных/аэропонных систем, теплиц, контейнеров.
     * Площадь: от 0.01 га (1 сотка) до 1 га
     *
     * Особенности:
     * - Можно выращивать в открытом грунте (как FIELD)
     * - Можно размещать мобильные теплицы
     * - Можно устанавливать гидропонные системы (NFT, DWC)
     * - Можно размещать контейнерные фермы
     * - Подходит для вертикального выращивания
     *
     * Пример: "Участок Приусадебный", 12 соток (0.12 га)
     */
    'PLOT' |

    /**
     * BED - Грядка
     * Физическая грядка в поле или теплице.
     * Может быть приподнятой, стационарной.
     * Имеет ширину, длину, площадь в м².
     *
     * Пример: "Грядка 1", 2м × 50м = 100 м²
     */
    'BED' |

    /**
     * ROW - Ряд
     * Линейная посадка растений (например, кукуруза, подсолнечник).
     * Характеризуется длиной и шириной междурядий.
     *
     * Пример: "Ряд 1-10", длина 500м
     */
    'ROW' |

// ========== ТЕПЛИЧНЫЕ ТИПЫ (Greenhouse) ==========
    /**
     * GREENHOUSE - Теплица
     * Закрытое сооружение для выращивания.
     * Может быть остекленной, поликарбонатной, пленочной.
     * Имеет климат-контроль, площадь в м².
     * Может содержать: GREENHOUSE_ZONE, BED, RACK
     *
     * Пример: "Теплица №1", 500 м²
     */
    'GREENHOUSE' |

    /**
     * GREENHOUSE_ZONE - Зона теплицы
     * Логическое разделение теплицы на зоны с разным микроклиматом.
     * Используется для разных культур или стадий роста.
     *
     * Пример: "Зона A (томаты)", "Зона B (огурцы)"
     */
    'GREENHOUSE_ZONE' |

// ========== КОНТЕЙНЕРНЫЕ ТИПЫ (Container-based) ==========
    /**
     * CONTAINER - Контейнер
     * Транспортабельная единица для вертикального выращивания.
     * Может быть морским контейнером, переоборудованным под ферму.
     * Имеет климат-контроль, освещение, автоматизацию.
     * Может содержать: RACK, SHELF, POT, TRAY
     *
     * Пример: "Контейнерная ферма A", 40ft
     */
    'CONTAINER' |

    /**
     * RACK - Стеллаж
     * Многоуровневая конструкция в контейнере или теплице.
     * Используется для вертикального выращивания.
     * Имеет количество уровней, расстояние между ними.
     * Может содержать: SLOT, POT, TRAY
     *
     * Пример: "Стеллаж №1", 4 уровня
     */
'RACK' |

    /**
     * SHELF - Полка
     * Отдельный уровень стеллажа.
     * Имеет площадь, высоту над уровнем пола.
     * Может содержать: POT, TRAY
     *
     * Пример: "Полка 2", уровень 2 из 4
     */
'SHELF' |

    /**
     * VERTICAL_TOWER - Вертикальная башня
     * Специализированная вертикальная конструкция.
     * Часто используется в аэропонике.
     * Растения располагаются по всей высоте.
     * Может содержать: SLOT
     *
     * Пример: "Башня A", высота 2м, 8 растений
     */
'VERTICAL_TOWER' |

// ========== ПОСАДОЧНЫЕ МЕСТА (Planting Slots) ==========
    /**
     * SLOT - Слот
     * Минимальная единица для одного растения.
     * Имеет конкретное положение на стеллаже/полке/башне.
     *
     * Пример: "Слот A1", положение (ряд 1, колонка 1)
     */
    'SLOT' |

    /**
     * POT - Горшок
     * Индивидуальная емкость для одного или нескольких растений.
     * Имеет объем, диаметр, высоту.
     * Может размещаться на стеллажах, полках.
     *
     * Пример: "Горшок 5л", диаметр 20см
     */
    'POT' |

    /**
     * TRAY - Лоток
     * Плоская емкость для множества растений (микрозелень, рассада).
     * Имеет площадь, глубину, количество ячеек.
     *
     * Пример: "Лоток 1020", 72 ячейки
     */
    'TRAY' |

    // ========== ГИДРОПОННЫЕ СИСТЕМЫ (Hydroponic) ==========
    /**
     * NFT_CHANNEL - NFT канал (Nutrient Film Technique)
     * Канал с тонким слоем питательного раствора.
     * Растения в горшочках, корни в растворе.
     * Имеет длину, ширину, уклон.
     *
     * Пример: "NFT канал 1", длина 10м
     */
    'NFT_CHANNEL' |

    /**
     * DWC_TANK - DWC бак (Deep Water Culture)
     * Бак с глубоким слоем питательного раствора.
     * Растения плавают на пенопластовой платформе.
     * Имеет объем, площадь поверхности.
     *
     * Пример: "DWC бак", 1000л, 2м²
     */
    'DWC_TANK' |

    /**
     * AEROPONIC_CHAMBER - Аэропонная камера
     * Герметичная камера с туманом из питательного раствора.
     * Корни растений висят в воздухе и опрыскиваются.
     * Имеет объем, количество форсунок, циклы опрыскивания.
     *
     * Пример: "Аэропонная камера", 1м³, 12 форсунок
     */
    'AEROPONIC_CHAMBER' |

    // ========== СЛУЖЕБНЫЕ ТИПЫ (Utility) ==========
    /**
     * RESERVOIR - Резервуар
     * Емкость для воды или питательного раствора.
     * Используется в гидропонных системах.
     * Имеет объем, материал, систему аэрации.
     *
     * Пример: "Резервуар 1", 500л
     */
    'RESERVOIR' |

    /**
     * STORAGE - Хранилище
     * Помещение для хранения урожая, инвентаря, удобрений.
     * Имеет площадь, объем, температурный режим.
     *
     * Пример: "Склад №1", 200м²
     */
    'STORAGE';